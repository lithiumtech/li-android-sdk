package com.lithium.community.android.model.response;

import com.google.gson.annotations.SerializedName;
import com.lithium.community.android.model.LiBaseModelImpl;

/**
 * Data Model for GET call to fetch sdk settings from community.
 * Created by shoureya.kant on 12/2/16.
 */

public class LiAppSdkSettings extends LiBaseModelImpl {

    private String type;
    private String id;
    @SerializedName("additional_information")
    private String additionalInformation;
    @SerializedName("app_type")
    private String appType;
    @SerializedName("client_id")
    private String clientId;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAdditionalInformation() {
        return additionalInformation;
    }

    public void setAdditionalInformation(String additionalInformation) {
        this.additionalInformation = additionalInformation;
    }

    public String getAppType() {
        return appType;
    }

    public void setAppType(String appType) {
        this.appType = appType;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }
}
